			</div>
			<div class="description_bts">

				<p>Le BTS SIO (Services Informatiques aux Organisations) est une formation post bac de deux années qui
					prépare les étudiants aux métiers de l'informatique et du numérique. Il propose ainsi deux
					options : SLAM (Solutions Logicielles et Applications Métiers) et SISR (Solutions
					d'Infrastructure, Systèmes et Réseaux).</p>

				<p>La formation couvre divers domaines tels que le développement web, la gestion de
					bases de données, la sécurité informatique, la gestion de projets informatiques,
					et bien plus encore.</p>

				<p>Le BTS SIO permet aux étudiants d'acquérir des compétences techniques
					ainsi que des compétences en communication et en gestion de projet, ce qui les
					prépare pour entrer sur le marché du travail ou bien poursuivre des études supérieures
					dans le domaine de l'informatique.</p>

				<div class="bts_notion">
					<p><img src="img/icon/duree.png">Durée de la formation : 2 ans</p>
					<p><img src="img/icon/diplome.png">Naturre de la formation : Diplôme national ou diplôme d'État</p>
					<p><img src="img/icon/etude.png">Niveau terminal d'études : Bac +2</p>
					<p><img src="img/icon/technicien.png">Ttype de formation : BTS (brevet de technicien supérieure)</p>
				</div>

				<div class="bts_info">

					<div class="bts_slam">
						<h1><img src="img/icon/dev.png">Option SLAM</h1>

						<p>Le BTS Services Informatiques aux Organisations (SIO) option Solutions Logicielles et
							Applications Métiers (SLAM) a pour objectif de former des spécialistes du développement
							d’applications. Les étudiants y apprennent à concevoir, développer et maintenir des
							solutions logicielles adaptées aux besoins des entreprises, en maîtrisant les langages
							de programmation, les bases de données et les méthodes de gestion de projet. À l’issue
							de la formation, ils sont capables de travailler en équipe et de répondre efficacement
							aux demandes techniques et fonctionnelles des clients.
						</p>
						<p>
							Conception et développement d'applications : conception, développement, déploiement et
							maintenance des composants logiciels d'une solution applicative (Web, mobile, client…) ;
							conception, développement et maintenance d'une solution applicative, bases de données,
							gestion des données numériques, gestion de versions (Git/forme logicielle).
						</p>
						<p>
							Cybersécurité des services informatiques : cybersécurisation d'une infrastructure réseau,
							d'un système ou d'un service et analyse des risques encourus par les entreprises en tenant
							compte des dimensions techniques, organisationnelles ou encore juridiques ; protection des
							données et identité numérique de l'entreprise, sécurisation des équipements et des usages.
						</p>
					</div>
					<div class="bts_sisr">
						<h1><img src="img/icon/ethernet.png">Option SISR</h1>

						<p>Le BTS Services Informatiques aux Organisations (SIO) option Solutions d’Infrastructure,
							Systèmes et Réseaux (SISR) forme des spécialistes des réseaux et des infrastructures
							informatiques. L’objectif est d’apprendre à installer, configurer, sécuriser et maintenir
							des équipements et services réseau, pour répondre aux besoins des entreprises.
						</p>
						<p>
							Administration des systèmes et des réseaux : modifier ou adapter les solutions
							d'infrastructure de réseau : concevoir une solution d'infrastructure réseau,
							l'installer, la tester et la déployer tout en assurant la qualité de service des
							équipements du réseau ; administrer et superviser ce réseau.
						</p>
						<p>
							Cybersécurité des services informatiques : cybersécurisation d'une infrastructure réseau,
